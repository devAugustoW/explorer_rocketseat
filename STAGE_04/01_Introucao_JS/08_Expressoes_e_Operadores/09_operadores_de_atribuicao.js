// Operador de atribuição (=)
let a = 5
console.log(a) // 5

// Operador de atribuição de adição (+=)
let b = 10
b += 5; // é o mesmo que b = b + 5
console.log(b) // 15

// Atribuição de subtração (-=)
let c = 20
c -= 3; // é o mesmo que c = c - 3
console.log(c) // 17

// Atribuição de multiplicação (*=)
let d = 8
d *= 2; // é o mesmo que d = d * 2
console.log(d) // 16

// Atribuição de divisão (/=)
let e = 50;
e /= 10; // é o mesmo que e = e / 10
console.log(e) // 5

// Atribuição de resto (%=)
let f = 25
f %= 7; // é o mesmo que f = f % 7
console.log(f) // 4